    private void tfApellidoAlumnoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfApellidoAlumnoKeyReleased
        List<Alumno> listaAlumno = alumnoData.listaAlumnosBaja(tfApellidoAlumno.getText());
        modelA.setRowCount(0);
        if (listaAlumno != null) {
            for (Alumno alumno : listaAlumno) {
                modelA.addRow(new Object[]{alumno.getIdAlumno(), alumno.getApellido(), alumno.getNombre()});
            }
        }
    }//GEN-LAST:event_tfApellidoAlumnoKeyReleased

    private void tfNombreMateriaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfNombreMateriaKeyReleased
        List<Materia> listaMateriasBajas = materiaData.listaMateriasBaja(tfNombreMateria.getText());
        modelM.setRowCount(0);
        if (listaMateriasBajas != null) {
            for (Materia materia : listaMateriasBajas) {
                modelM.addRow(new Object[]{materia.getIdMateria(), materia.getNombre(), materia.getAnioMateria()});
            }
        }
    }//GEN-LAST:event_tfNombreMateriaKeyReleased

