    private void btActivarAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btActivarAlumnoActionPerformed
        int selectedRow = tbAlumno.getSelectedRow();
        if (selectedRow >= 0) {
            int idAlumno = (int) tbAlumno.getValueAt(selectedRow, 0);
            alumnoData.activarAlumno(idAlumno);
            cleanAlumno();
        } else {
            JOptionPane.showMessageDialog(null, "No hay alumno seleccionado.");
        }
    }//GEN-LAST:event_btActivarAlumnoActionPerformed

